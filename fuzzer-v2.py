# Excluded file types
excluded_extensions = ['js', 'jpg', 'jpeg', 'png', 'gif', 'bmp', 'tiff', 'svg']

session = requests.Session()  # Reusing an HTTP session for performance

def fetch_js_urls(url, verify_ssl=False):
    headers = {'User-Agent': 'Mozilla/5.0'}
    try:
        response = session.get(url, headers=headers, timeout=5, verify=verify_ssl)
        soup = BeautifulSoup(response.text, 'html.parser')
        script_tags = soup.find_all('script')
        js_urls = [urljoin(response.url, tag.get('src')) for tag in script_tags if tag.get('src')]
        return js_urls
    except requests.exceptions.RequestException as e:
        logging.error(f"{Fore.RED}Failed to fetch URL {url}: {e}")
        return []

def download_js_content(url, verify_ssl=False):
    headers = {'User-Agent': 'Mozilla/5.0'}
    try:
        response = session.get(url, headers=headers, timeout=5, verify=verify_ssl)
        return response.text
    except requests.exceptions.RequestException as e:
        logging.error(f"{Fore.RED}Failed to download JS content from {url}: {e}")
        return None

def extract_paths(js_content):
    paths = re.findall(r'["\']\s*\/([a-zA-Z0-9_/?=&.-]+)\s*["\']', js_content)
    filtered_paths = [path for path in paths if not any(path.endswith(ext) for ext in excluded_extensions)]
    return filtered_paths

def extract_api_keys(js_content, js_url):
    results = []
    for key, pattern in _regex.items():
        matches = re.findall(pattern, js_content)
        for match in matches:
            results.append(f"{js_url} [{match}]")
    return results

def test_path_methods(base_url, path, verify_ssl=False):
    url = urljoin(base_url, path)
    try:
        response = session.get(url, timeout=5, verify=verify_ssl)  # Only GET request
        if response.status_code != 404:
            return f"{Fore.GREEN}{url} [GET][{response.status_code}][{len(response.content)}]"
    except requests.exceptions.RequestException as e:
        return f"{Fore.RED}{url} [GET][Error: {e}]"
    return None

def main():
    parser = argparse.ArgumentParser(description="Analyze JavaScript files for paths and sensitive data.")
    parser.add_argument("-u", "--url", help="URL to analyze")
    parser.add_argument("-l", "--list", help="File containing a list of URLs to analyze")
    parser.add_argument("-s", "--extract-secrets", action="store_true", help="Extract API keys from JavaScript files")
    parser.add_argument("-jp", "--just-paths", action="store_true", help="Extract just paths instead of full URLs")
    args = parser.parse_args()

    verify_ssl = False  # Default to false for SSL verification

    urls = []
    if args.url:
        urls.append(args.url)
    elif args.list:
        with open(args.list) as file:
            urls = [line.strip() for line in file.readlines()]

    if urls:
        results, api_keys = [], []
        with ThreadPoolExecutor(max_workers=20) as executor:
            futures = {executor.submit(fetch_and_process_js, url, verify_ssl, args.extract_secrets, args.just_paths): url for url in urls}
            for future in futures:
                paths_results, keys = future.result()
                results.extend(paths_results)
                api_keys.extend(keys)

        print("\nHere are the paths results:")
        for result in results:
            if result:
                print(result)
        if args.extract_secrets:
            print(f"\n{Fore.CYAN}Here are the API keys:")
            for key in api_keys:
                print(key)

def fetch_and_process_js(url, verify_ssl, extract_secrets, just_paths):
    js_urls = fetch_js_urls(url, verify_ssl)
    all_paths_results = []
    all_api_keys = []
    for js_url in js_urls:
        js_content = download_js_content(js_url, verify_ssl)
        if js_content:
            paths = extract_paths(js_content)
            if extract_secrets:
                api_keys = extract_api_keys(js_content, js_url)
                all_api_keys.extend(api_keys)
            for path in paths:
                path_result = test_path_methods(url, path, verify_ssl) if not just_paths else f"{Fore.BLUE}{urljoin(url, path)}"
                if path_result:
                    all_paths_results.append(path_result)
    return all_paths_results, all_api_keys

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    main()
